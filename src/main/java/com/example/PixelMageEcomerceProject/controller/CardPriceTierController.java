package com.example.PixelMageEcomerceProject.controller;

import com.example.PixelMageEcomerceProject.dto.request.CardPriceTierRequestDTO;
import com.example.PixelMageEcomerceProject.dto.response.ResponseBase;
import com.example.PixelMageEcomerceProject.entity.CardPriceTier;
import com.example.PixelMageEcomerceProject.service.interfaces.CardPriceTierService;
import io.swagger.v3.oas.annotations.Operation;
import io.swagger.v3.oas.annotations.media.Content;
import io.swagger.v3.oas.annotations.media.Schema;
import io.swagger.v3.oas.annotations.responses.ApiResponse;
import io.swagger.v3.oas.annotations.responses.ApiResponses;
import io.swagger.v3.oas.annotations.security.SecurityRequirement;
import io.swagger.v3.oas.annotations.tags.Tag;
import lombok.RequiredArgsConstructor;
import org.springframework.http.HttpStatus;
import org.springframework.http.ResponseEntity;
import org.springframework.web.bind.annotation.*;

import java.util.List;
import java.util.Optional;

@RestController
@RequestMapping("/api/card-price-tiers")
@RequiredArgsConstructor
@Tag(name = "Card Price Tier Management", description = "APIs for managing card price tiers")
@SecurityRequirement(name = "bearerAuth")
public class CardPriceTierController {

    private final CardPriceTierService cardPriceTierService;

    @PostMapping
    @Operation(summary = "Create a new card price tier", description = "Create a new card price tier")
    @ApiResponses(value = {
            @ApiResponse(responseCode = "201", description = "Card price tier created successfully",
                    content = @Content(schema = @Schema(implementation = ResponseBase.class))),
            @ApiResponse(responseCode = "400", description = "Bad request",
                    content = @Content(schema = @Schema(implementation = ResponseBase.class)))
    })
    public ResponseEntity<ResponseBase> createCardPriceTier(@RequestBody CardPriceTierRequestDTO cardPriceTierRequestDTO) {
        try {
            CardPriceTier createdTier = cardPriceTierService.createCardPriceTier(cardPriceTierRequestDTO);
            ResponseBase response = new ResponseBase(
                    HttpStatus.CREATED.value(),
                    "Card price tier created successfully",
                    createdTier
            );
            return ResponseEntity.status(HttpStatus.CREATED).body(response);
        } catch (Exception e) {
            ResponseBase response = new ResponseBase(
                    HttpStatus.BAD_REQUEST.value(),
                    "Failed to create card price tier: " + e.getMessage(),
                    null
            );
            return ResponseEntity.status(HttpStatus.BAD_REQUEST).body(response);
        }
    }

    @GetMapping
    @Operation(summary = "Get all card price tiers", description = "Retrieve all card price tiers")
    @ApiResponses(value = {
            @ApiResponse(responseCode = "200", description = "Card price tiers retrieved successfully",
                    content = @Content(schema = @Schema(implementation = ResponseBase.class)))
    })
    public ResponseEntity<ResponseBase> getAllCardPriceTiers() {
        List<CardPriceTier> tiers = cardPriceTierService.getAllCardPriceTiers();
        ResponseBase response = new ResponseBase(
                HttpStatus.OK.value(),
                "Card price tiers retrieved successfully",
                tiers
        );
        return ResponseEntity.ok(response);
    }

    @GetMapping("/{id}")
    @Operation(summary = "Get card price tier by ID", description = "Retrieve a card price tier by its ID")
    @ApiResponses(value = {
            @ApiResponse(responseCode = "200", description = "Card price tier found",
                    content = @Content(schema = @Schema(implementation = ResponseBase.class))),
            @ApiResponse(responseCode = "404", description = "Card price tier not found",
                    content = @Content(schema = @Schema(implementation = ResponseBase.class)))
    })
    public ResponseEntity<ResponseBase> getCardPriceTierById(@PathVariable Integer id) {
        Optional<CardPriceTier> tier = cardPriceTierService.getCardPriceTierById(id);
        if (tier.isPresent()) {
            ResponseBase response = new ResponseBase(
                    HttpStatus.OK.value(),
                    "Card price tier found",
                    tier.get()
            );
            return ResponseEntity.ok(response);
        } else {
            ResponseBase response = new ResponseBase(
                    HttpStatus.NOT_FOUND.value(),
                    "Card price tier not found with id: " + id,
                    null
            );
            return ResponseEntity.status(HttpStatus.NOT_FOUND).body(response);
        }
    }

    @GetMapping("/template/{templateId}")
    @Operation(summary = "Get card price tiers by template ID", description = "Retrieve all price tiers for a card template")
    @ApiResponses(value = {
            @ApiResponse(responseCode = "200", description = "Card price tiers found",
                    content = @Content(schema = @Schema(implementation = ResponseBase.class)))
    })
    public ResponseEntity<ResponseBase> getCardPriceTiersByTemplateId(@PathVariable Integer templateId) {
        List<CardPriceTier> tiers = cardPriceTierService.getCardPriceTiersByTemplateId(templateId);
        ResponseBase response = new ResponseBase(
                HttpStatus.OK.value(),
                "Card price tiers retrieved successfully",
                tiers
        );
        return ResponseEntity.ok(response);
    }

    @PutMapping("/{id}")
    @Operation(summary = "Update card price tier", description = "Update an existing card price tier")
    @ApiResponses(value = {
            @ApiResponse(responseCode = "200", description = "Card price tier updated successfully",
                    content = @Content(schema = @Schema(implementation = ResponseBase.class))),
            @ApiResponse(responseCode = "404", description = "Card price tier not found",
                    content = @Content(schema = @Schema(implementation = ResponseBase.class)))
    })
    public ResponseEntity<ResponseBase> updateCardPriceTier(@PathVariable Integer id, @RequestBody CardPriceTierRequestDTO cardPriceTierRequestDTO) {
        try {
            CardPriceTier updatedTier = cardPriceTierService.updateCardPriceTier(id, cardPriceTierRequestDTO);
            ResponseBase response = new ResponseBase(
                    HttpStatus.OK.value(),
                    "Card price tier updated successfully",
                    updatedTier
            );
            return ResponseEntity.ok(response);
        } catch (RuntimeException e) {
            ResponseBase response = new ResponseBase(
                    HttpStatus.NOT_FOUND.value(),
                    e.getMessage(),
                    null
            );
            return ResponseEntity.status(HttpStatus.NOT_FOUND).body(response);
        }
    }

    @DeleteMapping("/{id}")
    @Operation(summary = "Delete card price tier", description = "Delete a card price tier by ID")
    @ApiResponses(value = {
            @ApiResponse(responseCode = "200", description = "Card price tier deleted successfully",
                    content = @Content(schema = @Schema(implementation = ResponseBase.class))),
            @ApiResponse(responseCode = "404", description = "Card price tier not found",
                    content = @Content(schema = @Schema(implementation = ResponseBase.class)))
    })
    public ResponseEntity<ResponseBase> deleteCardPriceTier(@PathVariable Integer id) {
        try {
            cardPriceTierService.deleteCardPriceTier(id);
            ResponseBase response = new ResponseBase(
                    HttpStatus.OK.value(),
                    "Card price tier deleted successfully",
                    null
            );
            return ResponseEntity.ok(response);
        } catch (RuntimeException e) {
            ResponseBase response = new ResponseBase(
                    HttpStatus.NOT_FOUND.value(),
                    e.getMessage(),
                    null
            );
            return ResponseEntity.status(HttpStatus.NOT_FOUND).body(response);
        }
    }
}
